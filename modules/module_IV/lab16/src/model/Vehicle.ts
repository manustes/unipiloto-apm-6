export class Vehicle {
    
      brand: string;
      model: string;
      color: string;
      plate: string;
      capcity: string;
      build_year: number;

      constructor(brand: string, model: string, color: string, plate: string, capcity: string, build_year: number){

        this.brand = brand;
        this.model = model;
        this.color = color;
        this.plate = plate;
        this.capcity = capcity;
        this.build_year = build_year;

      }
    
}